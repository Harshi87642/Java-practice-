// Check Number is 3-digit or not


class hr64
{
  public static void main(String[] args)
  {
    
      int n=333;
  
      if(n>= 100 && n<=999)
         System.out.println(" It is a 3 digit number");
      else
          System.out.println(" It is not a 3 digit number");
  }
}



